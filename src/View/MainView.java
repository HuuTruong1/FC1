    public void showJPanel(String ac) {
        this.hiddenJPanels();

        if (ac.equals("CT hóa đơn")) {
            if (tbData.getSelectedRow() == -1) {
                pnQLHoaDon.setVisible(true);
                pnCTHD.setVisible(false);
            } else {
                pnCTHD.setVisible(true);
            }
        } else if (ac.equals("BÁO CÁO - THỐNG KÊ")) {
            pnBaoCao_TK.setVisible(true);
        } else if (ac.equals("TỔNG QUAN")) {
            pnTongQuan.setVisible(true);
        } else if (ac.equals("QUẢN LÝ HÓA ĐƠN")) {
            pnQLHoaDon.setVisible(true);
        } else if (ac.equals("Giới thiệu")) {
            pnGT.setVisible(true);
        }

    }

