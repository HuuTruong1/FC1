    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        if (checkEmptyInput()) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đủ dữ liệu!!!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            if (checkDate() == false) {
                JOptionPane.showMessageDialog(this, "Định dạng ngày tạo đã sai, mời nhập lại!!!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if (checkValidInput() == false) {
                JOptionPane.showMessageDialog(this, "Nhập chỉ số điện chưa đúng, mời nhập lại!!!", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
                return;
            }
            try {
                capNhatHoaDon();
                lamMoiOText();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

