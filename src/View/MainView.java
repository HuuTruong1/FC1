    public boolean checkDate() {
        boolean check = txtNgayTao.getText().matches("[a-zA-Z]");

        if (check) {
            return false;
        } else {
            String[] checkDate = txtNgayTao.getText().split("/");
            boolean check1 = checkDate[0].matches("\\d+");
            boolean check2 = checkDate[1].matches("\\d+");
            if (check1 == true && check2 == true || Integer.parseInt(checkDate[0]) > 0 && Integer.parseInt(checkDate[0]) < 13) {
                return true;
            } else {
                return false;
            }
        }
    }

    public boolean checkValidInput() {
        boolean check1 = txtCSCu.getText().matches("[a-zA-Z]");
        boolean check2 = txtCSMoi.getText().matches("[a-zA-Z]");
        if (check1 == true || check2 == true) {
            return false;
        } else {
            if (txtCSCu.getText().contains("-") || txtCSMoi.getText().contains("-")) {
                return false;
            } else {
                boolean check3 = Integer.valueOf(txtCSMoi.getText()) > Integer.valueOf(txtCSCu.getText());
                if (check3 == true) {
                    return true;
                } else {
                    return false;
                }
            }
        }

    }

